"""LLM-as-judge for end-to-end gateway quality evaluation.

Step 7 of the bench. Reads records from cache_bench_test.go's headline run
(JSONL with `{prompt, gateway_response, baseline_response, ...}`), labels
each row that has a non-empty `baseline_response` as YES (gateway response
is acceptable) or NO (gateway falls short and baseline is meaningfully
better). Quality-routed misses have empty `baseline_response` and are
skipped — they're already from the baseline model, no judgment needed.

Usage:
    uv run python label_eval.py [--input PATH] [--output PATH] [--workers N]

Requires GOOGLE_API_KEY (loaded from project root .env).
"""

from __future__ import annotations

import argparse
import json
import os
import threading
import time
from concurrent.futures import ThreadPoolExecutor, as_completed
from pathlib import Path

from dotenv import load_dotenv
from google import genai
from google.genai import types

SCRIPT_DIR = Path(__file__).parent
PROJECT_ROOT = SCRIPT_DIR.parent.parent
load_dotenv(PROJECT_ROOT / ".env")

JUDGE_MODEL = "gemini-2.5-pro"
MAX_OUTPUT_TOKENS = 8192
THINKING_BUDGET = 4096

DEFAULT_INPUT = SCRIPT_DIR.parent / "data" / "realistic_records.jsonl"
DEFAULT_OUTPUT = SCRIPT_DIR.parent / "data" / "realistic_records_labeled.jsonl"

INTER_PROMPT_DELAY = 0.25
MAX_RETRIES = 3
RETRY_BACKOFF = 2.0


RUBRIC_TEMPLATE = """\
You are evaluating the quality of a GATEWAY AI response against a
BASELINE AI response for the same user prompt.

The gateway response was returned by an LLM gateway that may have served
it from a semantic cache (a cached response originally generated for a
different but similar prompt) OR routed the prompt to a cheaper model
than the baseline. The baseline response was generated by the highest-
quality available model with no cache or cheap-routing involvement.

## User Prompt
{prompt}

## Gateway Response
{gateway_response}

## Baseline Response
{baseline_response}

## Task
Decide whether the gateway response is good enough to serve the user,
using the baseline response as a reference for what an answer to this
prompt should look like.

Answer YES if the gateway response adequately addresses the prompt AND
is at near parity with the baseline response (or better) — i.e. the user
would be well-served by either.

Answer NO if the gateway response fails to adequately address the
prompt AND the baseline response provides meaningfully better information.

Stylistic differences (tone, formatting, length) alone are not grounds
for NO — focus on whether the user gets the information they need.

Respond with JSON: {{"reasoning": "<brief explanation>", "label": "YES" or "NO"}}"""


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="LLM-as-judge for gateway quality eval")
    p.add_argument("--input", type=Path, default=DEFAULT_INPUT,
                   help="Records JSONL produced by cache_bench_test.go")
    p.add_argument("--output", type=Path, default=DEFAULT_OUTPUT,
                   help="Labeled records JSONL (appended; resumable)")
    p.add_argument("--workers", type=int, default=5,
                   help="Concurrent judge calls")
    return p.parse_args()


def judge_record(
    client: genai.Client, prompt: str, gateway: str, baseline: str
) -> dict | None:
    rubric = RUBRIC_TEMPLATE.format(
        prompt=prompt,
        gateway_response=gateway,
        baseline_response=baseline,
    )

    for attempt in range(MAX_RETRIES):
        try:
            response = client.models.generate_content(
                model=JUDGE_MODEL,
                contents=rubric,
                config=types.GenerateContentConfig(
                    thinking_config=types.ThinkingConfig(
                        thinking_budget=THINKING_BUDGET,
                    ),
                    response_mime_type="application/json",
                    max_output_tokens=MAX_OUTPUT_TOKENS,
                ),
            )

            result = json.loads(response.text)
            label = result.get("label")
            if label not in ("YES", "NO"):
                print(f"  Invalid label in judge output: {result!r}")
                return None
            return {"label": label, "reasoning": result.get("reasoning", "")}

        except Exception as e:
            wait = RETRY_BACKOFF ** attempt
            print(f"  Attempt {attempt + 1}/{MAX_RETRIES} failed ({e}), "
                  f"retrying in {wait:.0f}s...")
            time.sleep(wait)

    return None


def load_done_keys(output_path: Path) -> set[tuple[str, str]]:
    if not output_path.exists():
        return set()
    keys: set[tuple[str, str]] = set()
    with output_path.open() as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            entry = json.loads(line)
            keys.add((entry["prompt"], entry["gateway_response"]))
    return keys


def main() -> None:
    args = parse_args()

    api_key = os.environ.get("GOOGLE_API_KEY")
    if not api_key:
        raise SystemExit("GOOGLE_API_KEY not set (check .env at repo root)")
    if not args.input.exists():
        raise SystemExit(f"input file not found: {args.input}")

    client = genai.Client(api_key=api_key)

    all_records: list[dict] = []
    with args.input.open() as f:
        for line in f:
            line = line.strip()
            if line:
                all_records.append(json.loads(line))

    judgable = [r for r in all_records if r.get("baseline_response")]
    skipped_no_baseline = len(all_records) - len(judgable)

    done_keys = load_done_keys(args.output)
    remaining = [r for r in judgable
                 if (r["prompt"], r["gateway_response"]) not in done_keys]

    print(f"Loaded {len(all_records)} records: {len(judgable)} with baseline, "
          f"{skipped_no_baseline} without (quality-routed misses)")
    print(f"Already labeled: {len(judgable) - len(remaining)}, "
          f"remaining: {len(remaining)}")
    print(f"Judge: {JUDGE_MODEL}, workers: {args.workers}\n")

    if not remaining:
        print("Nothing to do.")
        return

    args.output.parent.mkdir(parents=True, exist_ok=True)

    write_lock = threading.Lock()
    total = len(remaining)
    completed = 0
    yes_count = 0
    no_count = 0
    skipped = 0

    def process(record: dict) -> None:
        nonlocal completed, yes_count, no_count, skipped

        result = judge_record(
            client,
            record["prompt"],
            record["gateway_response"],
            record["baseline_response"],
        )
        time.sleep(INTER_PROMPT_DELAY)

        with write_lock:
            completed += 1
            progress = f"[{completed}/{total}]"

            if result is None:
                skipped += 1
                print(f"{progress} SKIP — judge failed")
                return

            labeled = {
                **record,
                "label": result["label"],
                "judge_reasoning": result["reasoning"],
            }
            with args.output.open("a") as f:
                f.write(json.dumps(labeled) + "\n")

            if result["label"] == "YES":
                yes_count += 1
            else:
                no_count += 1
            reason = result["reasoning"][:80]
            tag = "HIT" if record.get("cache_hit") else "MISS"
            model = record.get("model_routed", "?")
            print(f"{progress} {result['label']:3s} {tag} {model[:25]:25s} — {reason}")

    with ThreadPoolExecutor(max_workers=args.workers) as pool:
        futures = [pool.submit(process, r) for r in remaining]
        for fut in as_completed(futures):
            fut.result()

    total_labeled = yes_count + no_count
    print(f"\nDone. {total_labeled} labeled, {skipped} skipped (judge failures).")
    if total_labeled:
        print(f"  YES: {yes_count} ({yes_count / total_labeled * 100:.1f}%)")
        print(f"  NO:  {no_count} ({no_count / total_labeled * 100:.1f}%)")


if __name__ == "__main__":
    main()
